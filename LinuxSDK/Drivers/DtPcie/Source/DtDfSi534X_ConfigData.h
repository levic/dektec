//  Design
//  ======
//  Host Interface:
//     I/O Power Supply: VDD (Core)
//     SPI Mode: 4-Wire
//     I2C Address Range: 104d to 107d / 0x68 to 0x6B (selected via A0/A1 pins)
//  
//  XA/XB:
//     54 MHz (XTAL - Crystal)
//  
//  Inputs:
//      IN0: Unused
//      IN1: 27 MHz
//           Standard
//      IN2: Unused
//      IN3: Unused
//  
//  Outputs:
//     OUT0: 148.3516483516483516... MHz [ 148 + 32/91 MHz ]
//           [ 148.5MHz*1000/1001 ]
//           Enabled, LVDS 1.8 V
//     OUT1: 148.5 MHz
//           Enabled, LVDS 1.8 V
//  
//  Frequency Plan
//  ==============
//  Fvco = 13.2759890109890109... GHz [ 13 + 5023/18200 GHz ]
//  Fpfd = 296.7032967032967032... kHz [ 296 + 64/91 kHz ]
//  Fms0 = 890.1098901098901098... MHz [ 890 + 10/91 MHz ]
//  Fms1 = 594 MHz
//  
//  P dividers:
//     P0  = Unused
//     P1  = 91
//     P2  = Unused
//     P3  = Unused
//     Pxaxb = 1
//  
//  MXAXB = 245.8516483516483516... [ 245 + 155/182 ]
//  M = 8949
//  N dividers:
//     N0:
//        Value: 14.915
//        OUT0: 148.3516483516483516... MHz [ 148 + 32/91 MHz ]
//     N1:
//        Value: 22.3501498501498501... [ 22 + 701/2002 ]
//        OUT1: 148.5 MHz [ 148 + 1/2 MHz ]
//  
//  R dividers:
//     R0 = 6
//     R1 = 4
//  
//  Nominal Bandwidth:
//    Desired: 100.000 Hz
//    Actual:  134.601 Hz
//    Coefficients:
//       BW0:  20
//       BW1:  35
//       BW2:  12
//       BW3:  11
//       BW4:  31
//       BW5:  63
//  Fastlock Bandwidth:
//    Desired: 4.000 kHz
//    Actual:  4.456 kHz
//    Coefficients:
//       BW0:  25
//       BW1:  47
//       BW2:  7
//       BW3:  6
//       BW4:  31
//       BW5:  63
//  Holdover Bandwidth:
//     N/A (Ramped Exit from Holdover)
//  
//  Digitally Controlled Oscillator (DCO)
//  =====================================
//  Mode: Register Direct Write
//  
//         Fvco * Nx_DEN
//  Fout = -------------
//          Nx_NUM * R
//  
//  N0: DCO Enabled
//  
//     Fvco:               13.2759890109890109... GHz [ 13 + 5023/18200 GHz ]
//     N0_NUM:             50046435328
//     N0_DEN:             3355443200
//     Step Word:          1
//  
//     Desired Step Size:  1 ppt
//     Actual Step Size:   19.981443103111... ppt
//     Range:              200 ppm
//  
//     OUT0
//          R0:            6
//          Initial Freq:  148.351648351648... MHz
//          Step Size:     0.00296428002... Hz
//          Min Freq:      148.321989887167... MHz
//          Max Freq:      148.381318679514... MHz
//  
//  N1: DCO Enabled
//  
//     Fvco:               13.2759890109890109... GHz [ 13 + 5023/18200 GHz ]
